dia = int(input("Digite um numero da semana de 1 a 7: "))
if dia == 1:
    print ("O dia selecionado é domingo")
if dia == 2:
    print ("O dia selecionado é segunda")
if dia == 3:
    print ("O dia selecionado é terça")    
if dia == 4:
    print ("O dia selecionado é quarta")
if dia == 5:
    print ("O dia selecionado é quinta")
if dia == 6:
    print ("O dia selecionado é sexta")
if dia == 7:
    print ("O dia selecionado é sabado")            